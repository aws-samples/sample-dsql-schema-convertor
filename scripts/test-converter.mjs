/**
 * Test suite for the DSQL converter.
 * These tests ensure the converter doesn't break after rule updates.
 * Exit 0 = all pass, Exit 1 = failures.
 */

import { convertSchema } from '../src/converter.js';

let passed = 0;
let failed = 0;

function test(name, input, assertions) {
    const result = convertSchema(input);
    const failures = [];

    for (const { check, description } of assertions) {
        if (!check(result)) {
            failures.push(description);
        }
    }

    if (failures.length === 0) {
        console.log(`  ✓ ${name}`);
        passed++;
    } else {
        console.log(`  ✗ ${name}`);
        failures.forEach(f => console.log(`      - ${f}`));
        failed++;
    }
}

console.log('Running converter tests...\n');

// --- SERIAL conversion ---
test('SERIAL → INTEGER IDENTITY', 'CREATE TABLE t (id SERIAL PRIMARY KEY);', [
    { check: r => r.sql.includes('INTEGER GENERATED BY DEFAULT AS IDENTITY'), description: 'Should convert SERIAL to INTEGER IDENTITY' },
    { check: r => r.sql.includes('CACHE 1'), description: 'Should include CACHE 1' },
    { check: r => !r.sql.includes('SERIAL'), description: 'Should not contain SERIAL' },
    { check: r => r.sql.includes('PRIMARY KEY'), description: 'Should preserve PRIMARY KEY' }
]);

test('BIGSERIAL → BIGINT IDENTITY', 'CREATE TABLE t (id BIGSERIAL PRIMARY KEY);', [
    { check: r => r.sql.includes('BIGINT GENERATED BY DEFAULT AS IDENTITY'), description: 'Should convert BIGSERIAL to BIGINT IDENTITY' }
]);

test('SMALLSERIAL → SMALLINT IDENTITY', 'CREATE TABLE t (id SMALLSERIAL PRIMARY KEY);', [
    { check: r => r.sql.includes('SMALLINT GENERATED BY DEFAULT AS IDENTITY'), description: 'Should convert SMALLSERIAL to SMALLINT IDENTITY' }
]);

// --- Sequences ---
test('Sequence kept with CACHE added', 'CREATE SEQUENCE my_seq START 1;', [
    { check: r => r.sql.includes('CREATE SEQUENCE'), description: 'Should keep CREATE SEQUENCE' },
    { check: r => r.sql.includes('CACHE 1'), description: 'Should add CACHE 1' }
]);

test('Sequence with valid CACHE untouched', 'CREATE SEQUENCE my_seq CACHE 65536 START 1;', [
    { check: r => r.sql.includes('CACHE 65536'), description: 'Should keep CACHE 65536 as-is' }
]);

test('Sequence with invalid CACHE fixed', 'CREATE SEQUENCE my_seq CACHE 100 START 1;', [
    { check: r => r.sql.includes('CACHE 65536'), description: 'Should fix CACHE 100 to CACHE 65536' },
    { check: r => !r.sql.includes('CACHE 100'), description: 'Should not contain CACHE 100' }
]);

// --- Foreign keys removed ---
test('Inline REFERENCES removed', 'CREATE TABLE t (id INT, customer_id INT REFERENCES customers(id));', [
    { check: r => !r.sql.includes('REFERENCES'), description: 'Should remove REFERENCES clause' },
    { check: r => r.sql.includes('customer_id INT'), description: 'Should keep the column itself' }
]);

test('FOREIGN KEY constraint removed', `CREATE TABLE t (id INT, fk INT, FOREIGN KEY (fk) REFERENCES other(id));`, [
    { check: r => !r.sql.includes('FOREIGN KEY'), description: 'Should remove FOREIGN KEY' },
    { check: r => !r.sql.includes('REFERENCES'), description: 'Should remove REFERENCES' }
]);

// --- Triggers removed ---
test('Trigger removed', `CREATE TRIGGER my_trig BEFORE UPDATE ON t FOR EACH ROW EXECUTE FUNCTION foo();`, [
    { check: r => !r.sql.includes('TRIGGER'), description: 'Should remove CREATE TRIGGER' },
    { check: r => r.changes.some(c => c.type === 'removed'), description: 'Should log a removed change' }
]);

// --- PL/pgSQL removed, SQL kept ---
test('PL/pgSQL function removed', `CREATE FUNCTION foo() RETURNS void AS $$ BEGIN NULL; END; $$ LANGUAGE plpgsql;`, [
    { check: r => !r.sql.includes('CREATE FUNCTION') && !r.sql.includes('plpgsql'), description: 'Should remove PL/pgSQL function' }
]);

test('SQL function preserved', `CREATE FUNCTION add_one(x INT) RETURNS INT AS $$ SELECT x + 1; $$ LANGUAGE SQL;`, [
    { check: r => r.sql.includes('CREATE FUNCTION'), description: 'Should keep SQL function' },
    { check: r => r.sql.includes('LANGUAGE SQL'), description: 'Should keep LANGUAGE SQL' }
]);

// --- Extensions removed ---
test('Extension removed', `CREATE EXTENSION IF NOT EXISTS "uuid-ossp";`, [
    { check: r => !r.sql.includes('EXTENSION'), description: 'Should remove CREATE EXTENSION' }
]);

// --- Partitioning removed ---
test('PARTITION BY removed', `CREATE TABLE t (id INT) PARTITION BY RANGE (id);`, [
    { check: r => !r.sql.includes('PARTITION'), description: 'Should remove PARTITION BY' },
    { check: r => r.sql.includes('CREATE TABLE t'), description: 'Should keep the table' }
]);

// --- Inheritance removed ---
test('INHERITS removed', `CREATE TABLE child (extra INT) INHERITS (parent);`, [
    { check: r => !r.sql.includes('INHERITS'), description: 'Should remove INHERITS' }
]);

// --- LISTEN/NOTIFY removed ---
test('LISTEN removed', `LISTEN my_channel;`, [
    { check: r => !r.sql.includes('LISTEN'), description: 'Should remove LISTEN' }
]);

// --- Identity columns get CACHE ---
test('Identity column gets CACHE', `CREATE TABLE t (id BIGINT GENERATED ALWAYS AS IDENTITY);`, [
    { check: r => r.sql.includes('CACHE 1'), description: 'Should add CACHE 1 to identity column' },
    { check: r => r.sql.includes('GENERATED ALWAYS AS IDENTITY'), description: 'Should keep IDENTITY' }
]);

// --- Normal table preserved ---
test('Valid DSQL table untouched (except header)', `CREATE TABLE users (id UUID PRIMARY KEY DEFAULT gen_random_uuid(), name VARCHAR(100) NOT NULL);`, [
    { check: r => r.sql.includes('CREATE TABLE users'), description: 'Should keep table' },
    { check: r => r.sql.includes('gen_random_uuid()'), description: 'Should keep gen_random_uuid()' },
    { check: r => r.sql.includes('VARCHAR(100)'), description: 'Should keep VARCHAR' },
    { check: r => r.changes.length === 0, description: 'Should have no changes' }
]);

// --- Summary ---
console.log(`\nResults: ${passed} passed, ${failed} failed\n`);

if (failed > 0) {
    process.exit(1);
} else {
    console.log('All tests passed ✓');
    process.exit(0);
}
